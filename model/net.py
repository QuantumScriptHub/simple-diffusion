class UpSample_add(nn.Sequential):
    def __init__(self, embedding_dim1, embedding_dim2):
        super(UpSample_add, self).__init__()
        self.convA = ConvModule(embedding_dim2, embedding_dim2, kernel_size=3, stride=1, padding=1)
        self.convB = ConvModule(embedding_dim2, embedding_dim1, kernel_size=3, stride=1, padding=1)

    def forward(self, x, concat_with):
        up_x = F.interpolate(x, size=[concat_with.size(2), concat_with.size(3)], mode='bilinear', align_corners=True)
        return self.convB(self.convA(torch.cat([up_x, concat_with], dim=1)))
